
public abstract class Item {

	private int posX;
	private int posY;

	public void pegar() {

	}

	public int getPosX() {// o método Get permite retornar os atributos.
		return posX;
	}

	public void setPosX(int posX) {//o método Set permite alterar.
		this.posX = posX;
	}

	public int getPosY() {
		return posY;
	}

	public void setPosY(int posY) {
		this.posY = posY;
	}

}
	
